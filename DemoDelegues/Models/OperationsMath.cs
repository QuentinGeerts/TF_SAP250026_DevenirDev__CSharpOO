namespace DemoDelegues.Models;

public static class OperationsMath
{
    public static double Addition (float a, float b)
    {
        return a + b; 
    }

    public static double Soustraction (float a, float b)
    {
        return a - b; 
    }

    public static double Multiplication(float a, float b)
    {
        return a * b;
    }
}
